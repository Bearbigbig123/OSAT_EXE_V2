"""
多語言翻譯管理系統
支援中文繁體 (ZH_TW) 和英文 (EN)
"""

class TranslationManager:
    """全域翻譯管理器 - 單例模式"""
    _instance = None
    _current_lang = "ZH_TW"  # 預設繁體中文 (可選: ZH_TW, EN, KO)
    
    def __new__(cls):
        if cls._instance is None:
            cls._instance = super().__new__(cls)
        return cls._instance
    
    def __init__(self):
        if not hasattr(self, '_initialized'):
            self._initialized = True
            self._observers = []  # 觀察者列表
    
    @property
    def current_lang(self):
        return self._current_lang
    
    @current_lang.setter
    def current_lang(self, lang):
        if self._current_lang != lang:
            self._current_lang = lang
            self._notify_observers()
    
    def register_observer(self, observer):
        """註冊觀察者（需要更新UI的 Widget）"""
        if observer not in self._observers:
            self._observers.append(observer)
    
    def unregister_observer(self, observer):
        """取消註冊觀察者"""
        if observer in self._observers:
            self._observers.remove(observer)
    
    def _notify_observers(self):
        """通知所有觀察者語言已改變"""
        for observer in self._observers:
            if hasattr(observer, 'refresh_ui_texts'):
                try:
                    observer.refresh_ui_texts()
                except Exception as e:
                    print(f"Error refreshing UI for {observer}: {e}")
    
    def get(self, key, default=""):
        """獲取翻譯文字"""
        if self._current_lang == "ZH_TW":
            translations = Translations.ZH_TW
        elif self._current_lang == "EN":
            translations = Translations.EN
        elif self._current_lang == "KO":
            translations = Translations.KO
        else:
            translations = Translations.ZH_TW
        return translations.get(key, default)
    
    def toggle_language(self):
        """切換語言 (循環: 中文 -> English -> 한국어)"""
        if self._current_lang == "ZH_TW":
            self.current_lang = "EN"
        elif self._current_lang == "EN":
            self.current_lang = "KO"
        else:  # KO
            self.current_lang = "ZH_TW"
        return self.current_lang


